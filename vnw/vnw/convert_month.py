def convert(month):
    if month == 'January':
        return '1'
    if month == 'February':
        return '2'
    if month == 'March':
        return '3'
    if month == 'April':
        return '4'
    if month == 'May':
        return '5'
    if month == 'June':
        return '6'
    if month == 'July':
        return '7'
    if month == 'August':
        return '8'
    if month == 'September':
        return '9'
    if month == 'October':
        return '10'
    if month == 'November':
        return '11'
    if month == 'December':
        return '12'
